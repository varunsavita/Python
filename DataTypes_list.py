# Lists store multiple data together in a single variable.

# Append() : Adds an item to the end of the list
# Extend() : Adds items of lists and other iterables to the end of the list.
# Insert() : Inserts an item at the specified index.
# Remove() : Removes item present at the given index.
# Pop() : Returns and removes item

data_num = [19, 26, 29]
print(data_num)

data_num = ['varun', 'kumar', 'savita']
print(data_num)

print(data_num[2])
print(data_num[-3])
# Slicing of a list
print(data_num[1:2])

print(data_num[:])

# Check if an item exists in the list
print('varun' in data_num)

